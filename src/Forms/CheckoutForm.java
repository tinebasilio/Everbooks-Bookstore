    private void btnDoneMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDoneMouseClicked
        if(paymentMethods.getSelection() == null ||
                txtName.getText().isBlank() ||
                txtCardNumber.getText().isBlank() ||
                txtName2.getText().isBlank() ||
                txtAddress.getText().isBlank() ||
                txtCity.getText().isBlank() ||
                txtZipCode.getText().isBlank())
        {
                JOptionPane.showMessageDialog(this, "Please fill up the form.");
        }
        else {
            isDone = true;
            name = txtName.getText();
            lblName.setText(name);

            date = LocalDate.now().format(DateTimeFormatter.ofPattern("MMM-dd-yyyy"));
            System.out.println(date); 

            paymentMethod = this.paymentMethods.getSelection().getActionCommand();
            System.out.println(paymentMethod);
            ImageIcon icon = new ImageIcon(String.format("C:\\Users\\Lifted Laptop\\Documents\\NetBeansProjects\\"
                    + "FinalProjectBS\\src\\Forms\\logo-%s.png", paymentMethod)); //change path 
            icon.getImage().flush();
            iconPaymentMethod.setIcon(icon);

            address = String.format("%s, %s %s", txtAddress.getText(), txtCity.getText(), txtZipCode.getText());
            lblAddress.setText(address); 
        }
    }//GEN-LAST:event_btnDoneMouseClicked

    private void btnPayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPayMouseClicked
        // TODO add your handling code here:
        if(isDone == false){
            JOptionPane.showMessageDialog(this, "Please fill up the form.");
        }
        else{
            int total = MainMenuUI.totalPrice + 100;
            new fileHandling().write(name, date, paymentMethod, address, bookOrders, total);

            int option = JOptionPane.showConfirmDialog(this, "Go back to main menu?", "Payment Successful!", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if (option == 0){
                dispose();
                new MainMenuUI().setVisible(true);
            }  
            else
                dispose();
        } 
    }//GEN-LAST:event_btnPayMouseClicked


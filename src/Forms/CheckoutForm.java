    public void updateListOfItems(){
        lblNumOfItems.setText(String.format("Items (%s)", MainMenuUI.numOfItems.getText()));
        
        if (Integer.valueOf(MainMenuUI.numOfItems.getText()) > 4)
            jScrollPane1.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_ALWAYS);
        
        int i = 0, j = 0, price, qty; //i is for what panel to show, j is for what content of the panel to show
        String stringPrice, bookOrder, title;
        for (JSpinner spn : MainMenuUI.itemQty){
            qty = (int) spn.getValue();
            if(qty != 0){
                pnlItem[i].setVisible(true);
                title = MainMenuUI.title.get(j);
                bookNames[i].setText(title);
                itemQty[i].setText(String.format("(%d)", qty));
                price = MainMenuUI.price.get(j) * qty;
                stringPrice = String.format((price <= 999)? "        ₱%d" : (price <= 9999)? "      ₱%d" : "    ₱%d", price);
                itemPrices[i].setText(stringPrice);
                bookOrder = String.format("(%d) %s ₱%d", qty, title, price);
                bookOrders.add(bookOrder);
                i++;
            }
            j++;
        }
        
        int subTotal = MainMenuUI.totalPrice;
        lblSubtotal.setText(String.format("₱%d", subTotal));
        lblTotal.setText(String.valueOf(subTotal + 100));
    }
